    private void setIndices(int index){
        
        if (CLICKS == 1){
            indexTurn[0]= index;
            
        }
        else if (CLICKS ==2){
          indexTurn[1]= index;
        }
    }
    
    private boolean checkMatch(){
        
        if (symbols[indexTurn[0]-1]== symbols[indexTurn[1]-1]){
            matchedGame++;
          return true;
        }
        else
            return false;
    }
    private void afterClick(){
        JButton buttons []= {jb1, jb2, jb3, jb4, jb5, jb6, jb7, jb8, jb9, jb10, jb11, jb12};
        
        if (CLICKS == 2){
            matched = checkMatch();
            CLICKS =0;
            
            if (tCounter % 2 != 0){
                if (matched == true){
                     P1.setMatches(P1.getMatches()+1);
                     jlInstructions.setText(P1.getName() + ": Yay, you made a match! You get another turn.");
                     jlP1Matches.setText(Integer.toString(P1.getMatches()));
                     jbNext.setVisible(true);
                        card.disableCards(board, buttons);
                        
                     
                    }
                else {
                    jlInstructions.setText(P1.getName() + ": Sorry, not a match. Next player's turn.");
                    jbNext.setVisible(true);
                        card.disableCards(board, buttons);
            
                    //next player's turn
                    tCounter++;} 
        }
             
            else {
                if (matched == true){
                    P2.setMatches(P2.getMatches()+1);
                    jlInstructions.setText(P2.getName() + ": Yay, you made a match! You get another turn.");
                    jlP2Matches.setText(Integer.toString(P2.getMatches()));
                    jbNext.setVisible(true);
                        card.disableCards(board, buttons);
                    
                    }
                else{
                    //jbLButton1.setText("1"); 
                    jlInstructions.setText(P2.getName() + ": Sorry, not a match. Next player's turn.");
                    jbNext.setVisible(true);
                        card.disableCards(board, buttons);
            
                    //next player's turn
                    tCounter++;} 
            }
        }
        else{
            if (tCounter % 2 != 0){
              jlInstructions.setText(P1.getName() + ": Choose another card.");  
            }
            else{
              jlInstructions.setText(P2.getName() + ": Choose another card.");
            }
        }
            
    }
    
    private void setBoard(int size,  JButton [] button){
        JButton buttons []= {jb1, jb2, jb3, jb4, jb5, jb6, jb7, jb8, jb9, jb10, jb11, jb12};
        
        for (int i = 1, j=0; i < size; i++, j++){
          if (matched == false){
            if (indexTurn[0]==i || indexTurn[1]==i){
               button[j].setText(Integer.toString(i)); 
            }
                     
            matched = false;
            jbNext.setVisible(false); 
                card.enableCards(board, buttons);
                
                //player instructions
                if (tCounter % 2 != 0){
                    jlInstructions.setText(P1.getName() + ": Choose a card.");  
                    }
                else{
                    jlInstructions.setText(P2.getName() + ": Choose a card.");
                    }
                }
        else if (matched == true){
            for (i = 1, j=0; i < size; i++, j++){
            if (indexTurn[0]==i || indexTurn[1]==i){
               button[j].setVisible(false);
            }
            
            jbNext.setVisible(false);
                card.enableCards(board, buttons);
                
                //player instructions
                if (tCounter % 2 != 0){
                    jlInstructions.setText(P1.getName() + ": Choose a card.");  
                    }
                else{
                    jlInstructions.setText(P2.getName() + ": Choose a card.");
                    }
                }
             
        }
        }
    }
      
    
